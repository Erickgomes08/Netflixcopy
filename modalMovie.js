// Set up click handlers for all carousel items
document.querySelectorAll('.carrossel-item').forEach(item => {
    item.addEventListener('click', function() {
        const img = this.querySelector('img');
        const movieTitle = img.alt;
        
        if (movieDatabase[movieTitle]) {
            const movie = movieDatabase[movieTitle];
            document.getElementById('modalPoster').src = movie.poster;
            document.getElementById('modalTitle').textContent = movieTitle;
            document.getElementById('modalRating').textContent = movie.rating;
            document.getElementById('modalYear').textContent = movie.year;
            document.getElementById('modalDuration').textContent = movie.duration;
            document.getElementById('modalGenre').textContent = movie.genre;
            document.getElementById('modalSynopsis').textContent = movie.synopsis;
            
            document.getElementById('movieModal').style.display = 'block';
            document.body.style.overflow = 'hidden'; // Prevent scrolling
        }
    });
});

// Close modal functionality
document.querySelector('.close-modal').addEventListener('click', function() {
    document.getElementById('movieModal').style.display = 'none';
    document.body.style.overflow = 'auto';
});

// Close when clicking outside modal
window.addEventListener('click', function(event) {
    if (event.target == document.getElementById('movieModal')) {
        document.getElementById('movieModal').style.display = 'none';
        document.body.style.overflow = 'auto';
    }
});